<script>
  import Search from './components/Search'
  import {
    FilterIcon,
    XIcon,
    PlayCircleIcon,
    SearchIcon,
    TrashIcon,
    ServerIcon,
    UploadCloudIcon,
    DownloadCloudIcon,
    PauseCircleIcon,
    SettingsIcon,
    LoaderIcon,
    SquareIcon,
    CheckSquareIcon,
    SaveIcon
  } from 'vue-feather-icons'
  import formatJSON from 'json-format-highlight'
  import msgpack from 'msgpack-lite'

  const HOST = location.port === '1234' ? 'localhost:3000' : location.host

  // TODO: Detect updates with npm
  export default {
    name: 'MinecraftPacketDebugger',
    components: {
      FilterIcon,
      XIcon,
      PlayCircleIcon,
      SearchIcon,
      TrashIcon,
      ServerIcon,
      UploadCloudIcon,
      DownloadCloudIcon,
      PauseCircleIcon,
      LoaderIcon,
      SettingsIcon,
      SquareIcon,
      CheckSquareIcon,
      SaveIcon,
      Search
    },
    methods: {
      formatJSON (...args) {
        return formatJSON(...args)
      },
      open (packet) {
        this.id_tab = 0

        if(!this.settings.website.open_multiple_packets){
          if (this.open_packets.size > 0) {
            return this.open_packets.clear()
          }
          this.open_packets = []
          this.open_packets.push(packet)
        } else {
          if (this.open_packets.includes(packet)){
            const index = this.open_packets.indexOf(packet);
            return this.open_packets.splice(index, 1);
          }
          this.open_packets.push(packet);
        }
        window.packet = this.open_packets
      },

      isFiltered (packet) {

        const words = this.search.query.split(' ').filter(s => s.length)
        const clientIndex = words.indexOf('to:client')
        const serverIndex = words.indexOf('to:server')
        const toClient = !!~clientIndex && !!words.splice(clientIndex, 1).length
        const toServer = !!~serverIndex && !!words.splice(serverIndex, 1).length

        if (toClient && toServer) {
          return false
        }

        if (toClient && (packet.to !== 0)) {
          return false
        }

        if (toServer && (packet.to !== 1)) {
          return false
        }

        if (!words.length) {
          return !this.filters[packet.meta.name]
        } else if (!words.includes(packet.meta.name)) {
          return false
        }

        return true
      },

      startRecording () {
        this.packets = []
        this.record.state = 1
        this.record.messageQueue = []

        this.ws = new WebSocket(`ws://${HOST}/ws`)

        this.ws.onmessage = (message) => {
          requestAnimationFrame(() => {
            if (this.record.messageQueue.length > 2048) {
              this.stopRecording()
            }

            this.record.messageQueue.push(message.data)
          })
        }
      },
      stopRecording () {
        if (this.ws) {
          this.ws.close()
          this.ws = null
          this.record.state = 2

          const copyPackets = async () => {
            if (this.record.messageQueue.length === 0) {
              this.record.state = 0
              return
            }

            const chunk = this.record.messageQueue.splice(0, 32)
            const arrayBuffers = await Promise.all(chunk.map(fn => fn.arrayBuffer()))

            const packets = arrayBuffers.map(buf => msgpack.decode(new Uint8Array(buf)).packet)
            this.packets.push(...packets)

            requestAnimationFrame(copyPackets)
          }

          requestAnimationFrame(copyPackets)
        }
      },
      settingsSelectPacket (packets) {
        for (const packet of packets) {
          this.settings.ignoredPackets.custom[packet] = true
        }

        this.$nextTick(() => {
          this.search.query = ''
        })
      },
      async loadSettings() {
        const response = await fetch(`http://${HOST}/settings`)
        this.settings = await response.json()
      },
      async openSettings () {
        this.settings_opened = true;
      },
      async closeSettings () {
        const { settings: body } = this
        this.search.query = ''

        // Waiting for server to restart everything
        const res = await fetch(`http://${HOST}/settings`, {
          method: 'post',
          body: JSON.stringify(body)
        })

        const data = await res.json()

        if (data.error) {
          // TODO: Add some fancy notification
          console.error(data.error)
        } else {
          this.version = data.version
          this.host = body.server.host

          if (data.packets) {
            this.search.packets = data.packets
          }
        }
        this.settings_opened = false
        console.log(this.settings.website.open_multiple_packets)
        // await this.loadSettings()
        // this.settings = null
      },
    },
    data () {
      return {
        search: {
          query: '',
          packets: {
            toServer: [],
            toClient: []
          }
        },
        listening: false,
        open_packets: [],
        version: '0.0.0',
        host: 'localhost',
        id_tab: 0,
        ws: null,
        settings: null,
        record: { messageQueue: [], state: -1 },
        tabs: [ 'Packet', 'Meta', 'Format' ],
        packets: [],
        filters: {},
        settings_opened: false
      }
    },
    async created () {
      const res = await fetch(`http://${HOST}/host`)
      const data = await res.json()
      this.version = data.version
      this.host = data.host

      if (data.packets) {
        this.search.packets = data.packets
      }
      await this.loadSettings()
    },
  }
</script>
